CREATE TABLE position (
	pid INTEGER PRIMARY KEY NOT NULL,
	pname char(10) DEFAULT NULL
);

CREATE TABLE scouter (
	sid INTEGER PRIMARY KEY NOT NULL,
	sname char(10) DEFAULT NULL,
	sphone char(10) DEFAULT NULL,
	pid INTEGER NOT NULL,
 	FOREIGN KEY (pid)
              REFERENCES position (pid)
);

CREATE TABLE staff (
	stid INTEGER PRIMARY KEY NOT NULL,
	stname char(10) DEFAULT NULL,
	stphone char(10) DEFAULT NULL,
 	sid INTEGER NOT NULL,
 	FOREIGN KEY (sid)
              REFERENCES scouter (sid)
);

CREATE TABLE player (
	plid INTEGER PRIMARY KEY NOT NULL,
	plname char(10) DEFAULT NULL,
	plpos char(10) DEFAULT NULL,
 	sid INTEGER NOT NULL,
 	FOREIGN KEY (sid)
              REFERENCES scouter (sid)
);


CREATE TABLE schedule (
	scid INTEGER PRIMARY KEY NOT NULL,
 	plid INTEGER NOT NULL,
	date char(10) DEFAULT NULL,
	stype char(10) DEFAULT NULL,
 	stid INTEGER NOT NULL,
 	FOREIGN KEY (plid)
              REFERENCES player (plid)
 	FOREIGN KEY (stid)
              REFERENCES staff (stid)
);

